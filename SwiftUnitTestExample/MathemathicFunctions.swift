import Foundation

class MathemathicFunctions{
    
    func addIntegers(x: Int, y:Int) -> Int{
        return x + y
    }
    
    func multiplyIntegers(x: Int, y:Int) -> Int{
        return x * y
    }
    
    func divideIntegers(x: Int, y:Int) -> Int{
        return x / y
    }
    
    func subtractIntegers(x: Int, y:Int) -> Int{
        return x - y
    }
    
}
